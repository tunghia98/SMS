    public boolean validateFormClass() {
        if (txtClassID.getText().isEmpty() || txtClassName.getText().isEmpty() || txtClassID.getText().isEmpty()
                || txtClassTeacher.getText().isEmpty() || txtClassSchoolYear.getText().isEmpty()
                || txtClassTeacher.getText().isEmpty()) {
            return false;
        }
        return true;
    }

    public void setModelClass(Class class1) {
        txtClassID.setText(class1.getClassID());
        txtClassName.setText(class1.getClassName());
        txtClassGrade.setText(class1.getGradeID());
        txtClassQuantity.setText(class1.getQuantity() + "");
        txtClassSchoolYear.setText(class1.getShoolyear());
        txtClassTeacher.setText(class1.getTeacherID());
    }

    public Class getModelClass() {
        Class class1 = new Class();
        class1.setClassID(txtClassID.getText());
        class1.setClassName(txtClassName.getText());
        class1.setGradeID(txtClassGrade.getText());
        if(Integer.valueOf(txtClassQuantity.getText())>40){
            txtClassQuantity.setText("40");
        }
        else
        class1.setQuantity(Integer.parseInt(txtClassQuantity.getText()));
        class1.setShoolyear(txtClassSchoolYear.getText());
        class1.setTeacherID(txtClassTeacher.getText());
        return class1;
    }

    public void resetClass() {
        txtClassID.setText("");
        txtClassName.setText("");
        txtClassGrade.setText("");
        txtClassQuantity.setText("");
        txtClassSchoolYear.setText("");
        txtClassTeacher.setText("");
    }


