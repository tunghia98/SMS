    private void loadDataTableClass() {
        DefaultTableModel model = (DefaultTableModel) classList.getModel();
        model.setRowCount(0); // xóa table
        for (Class class1 : classBUS.getAllClass()) {
            Object dataRow[] = new Object[6];
            dataRow[0] = class1.getClassID();
            dataRow[1] = class1.getClassName();
            dataRow[2] = class1.getGradeID();
            dataRow[3] = class1.getQuantity();
            dataRow[4] = class1.getShoolyear();
            dataRow[5] = class1.getTeacherID();
            model.addRow(dataRow);
        }

    }

    private void loadDataTableGrade() {
        DefaultTableModel gmodel = (DefaultTableModel) gradeList.getModel();
        gmodel.setRowCount(0); // xóa table 
        for (Grade grade : gradeBUS.getAllGrades()) {
            Object gdataRow[] = new Object[2];
            gdataRow[0] = grade.getGradeID();
            gdataRow[1] = grade.getGradeName();
            gmodel.addRow(gdataRow);
            System.out.println(grade.toString());
        }
    }

    private void loadDataClassFactor(String[] factor) {
        DefaultTableModel model = (DefaultTableModel) classList.getModel();
        model.setRowCount(0); // xóa table
        for (Class class1 : classBUS.getClassFactor(factor)) {
            Object dataRow[] = new Object[6];
            dataRow[0] = class1.getClassID();
            dataRow[1] = class1.getClassName();
            dataRow[2] = class1.getGradeID();
            dataRow[3] = class1.getQuantity();
            dataRow[4] = class1.getShoolyear();
            dataRow[5] = class1.getTeacherID();
            model.addRow(dataRow);
            System.out.println(class1.toString());
        }
    }

